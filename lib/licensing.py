# Machine fingerprinting
# ---------------------------------------------------------------------------

def generate_machine_fingerprint() -> str:
    """Generate a stable machine fingerprint from hardware identifiers.

    Combines MAC address, hostname, platform string, processor, and
    machine-id (Linux) or MachineGuid (Windows) into a single
    ``sha256:<hex>`` digest.

    SECURITY NOTE: Machine fingerprinting is a commercial deterrent, not
    a cryptographic binding.  A user with root/admin access can spoof
    every component.  The fingerprint prevents casual license sharing
    (e.g. copying license.json to a colleague's laptop) but will not
    stop a determined attacker.

    TODO: Consider adding SMBIOS/BIOS serial and TPM-backed attestation
    for higher-assurance environments.
    """
    parts: List[str] = []

    # MAC address (first available NIC).
    # Note: uuid.getnode() may return a random MAC on some virtualised
    # environments; this is acceptable for our purposes.
    mac = uuid.getnode()
    parts.append(f"mac:{mac:012x}")

    # Hostname -- changes if the user renames the machine.
    parts.append(f"host:{platform.node()}")

    # Platform identifier (OS + architecture).
    parts.append(f"platform:{platform.system()}-{platform.machine()}")

    # Processor string -- may be empty on some platforms.
    parts.append(f"cpu:{platform.processor()}")

    # OS-specific machine ID for extra stability across reboots.
    machine_id = _read_machine_id()
    if machine_id:
        parts.append(f"mid:{machine_id}")

    combined = "|".join(parts).encode("utf-8")
    digest = hashlib.sha256(combined).hexdigest()
    return f"sha256:{digest}"


def _read_machine_id() -> str:
    """Read the OS machine ID if available. Returns empty string on failure.

    Linux: /etc/machine-id or /var/lib/dbus/machine-id (systemd-generated)
    Windows: HKLM\\SOFTWARE\\Microsoft\\Cryptography\\MachineGuid
    """
    # Linux paths -- try the primary location first, then the D-Bus fallback.
    for path in ("/etc/machine-id", "/var/lib/dbus/machine-id"):
        try:
            content = Path(path).read_text(encoding="utf-8").strip()
            if content:
                return content
        except OSError:
            continue

    # Windows: read MachineGuid from the registry.
    if platform.system() == "Windows":
        try:
            import winreg
            key = winreg.OpenKey(
                winreg.HKEY_LOCAL_MACHINE,
                r"SOFTWARE\Microsoft\Cryptography",
            )
            try:
                value, _ = winreg.QueryValueEx(key, "MachineGuid")
                return str(value)
            finally:
                # Always close the registry key to avoid resource leaks.
                winreg.CloseKey(key)
        except OSError:
            pass

    return ""


